	AREA	RESET,	CODE
		ENTRY
		LDR R0,=N
		LDR R1,[R0]
		LDR R0,=NUMS
		LDR R2,[R0]  ;INITAILISING FIRST ELEMENT AS SMALLEST
		
		;PRG FOR SMALLEST 
		CMP R1,#1
		BEQ EXIT
		
SMALL
		LDR R3,[R0],#4
		CMP R3,R2
		MOVLT R2,R3		
		SUBS R1,#1
		BNE SMALL
		
EXIT 
		LDR R0,=SMALLEST
		STR R2,[R0]
		
		; PRG FOR GREATEST
		
		LDR R0,=N
		LDR R1,[R0]
		LDR R0,=NUMS
		LDR R4,[R0]  ;INITAILISING FIRST ELEMENT AS GREATEST
		
		CMP R1,#1
		BEQ LBL
		
BIG
		LDR R3,[R0],#4
		CMP R3,R4
		MOVGT R4,R3		
		SUBS R1,#1
		BNE BIG
		
LBL 
		LDR R0,=GREATEST
		STR R4,[R0]
STOP	B STOP
N DCD &6
NUMS DCD &2,&4,&6,&3,&1,&0
	
	AREA	RESET,	DATA
SMALLEST SPACE 4
GREATEST SPACE 4
		END
		